import javax.swing.*;

/**
 * Customizable JPanel for GameFrame.
 * It is shown when the program is started. The user can choose if local or multiplayer game should be played.
 */
public class MenuPanel extends JPanel {
    /**
     * Constructor for class MenuPanel.
     * It allows easy customizability.
     */
    public MenuPanel() {
        setFocusable(true);
    }
}
