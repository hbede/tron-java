import javax.swing.*;
/**
 * Customizable JPanel for GameFrame.
 * It is shown when multiplayer option is chosen. It asks for the IP address of the other machine, your port number,
 * and the port number of the other machine.
 */
public class MultiPanel extends JPanel {

    /**
     * Constructor for class MultiPanel.
     * Allows customizability.
     */
    public MultiPanel() {
        setFocusable(true);
    }
}
